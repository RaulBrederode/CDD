a = int(input("Digite o 1° valor: "))
b = int(input("Digite o 2° valor: "))
c = int(input("Digite o 3° valor: "))

soma = a + b

if soma > c:
    print("A soma entre o 1° e o 2° valor é maior que o 3° valor.")
if soma < c:
    print("A soma entre o 1° e o 2° valor é menor que o 3° valor.")
